#include "ft_printf.h"

void	ft_putusign(unsigned int n, int fd)
{
	if (n >= 10)
	{
		ft_putusign(n / 10, fd);
		n = n % 10;
	}
	if (n < 10)
	{
		ft_putchar_fd(n + 48, fd);
	}
}
